const num = 5

switch (num) {
    case 1://string or any other datatype can also be written in place of 1
        console.log("Monday");    
        break;
    case 2:
        console.log("Tuesday");    
        break;
    case 3:
        console.log("Wednesday");    
        break;
    case 4:
        console.log("Thursday");    
        break;
    default:
        console.log("Enter valid data");
        break;
}